    /**
     * Calcular desconto e alterar valores dos campos relacionados.
     */
    private void calcValorTotal() {
        try {
            int desconto = Integer.parseInt(txtDesconto.getText());
            if (desconto <= 100 && desconto >= 0) {
                double valorConsulta = MaskFormatTextUtil.changeCommaByDot(txtValorConsulta.getText());
                double valorTotal = valorConsulta - ((valorConsulta / 100) * desconto);
                txtValorTotal.setText(MaskFormatTextUtil.changeDotByComma(String.format("%.2f", valorTotal)));
                checkForm2();
            } else {
                JOptionPane.showMessageDialog(this, "O desconto deve estar no intervalo de 0-100%.");
                resetValorTotal();
            }
        } catch (NumberFormatException numberFormatException) {
            JOptionPane.showMessageDialog(this, "Apenas nÃºmeros inteiros sÃ£o permitidos para o desconto.");
            resetValorTotal();
        }
    }

    /**
     * Reseta o campo de desconto e desativa o botÃ£o de faturar.
     */
    private void resetValorTotal() {
        txtDesconto.setText("0");
        btnFaturar.setEnabled(false);
    }

    /**
     * vÃ¡lidar formulÃ¡rio e caso necessÃ¡rio informa o user sobre o problema.
     *
     * @return
     */
    private boolean checkForm() {
        if (jcbFormaPagamento.getSelectedIndex() == -1) {
            JOptionPane.showMessageDialog(this, "Informe uma forma de pagamento.");
            return false;
        }
        if (txtDesconto.getText().equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(this, "Informe um valor de desconto. Caso nÃ£o haja, informe 0.");
            return false;
        }
        btnFaturar.setEnabled(true);
        return true;
    }

    /**
     * vÃ¡lidar formulÃ¡rio e libera o botÃ£o de faturar
     *
     * @return
     */
    private boolean checkForm2() {
        if (jcbFormaPagamento.getSelectedIndex() == -1) {
            return false;
        }
        if (txtDesconto.getText().equalsIgnoreCase("")) {
            return false;
        }
        btnFaturar.setEnabled(true);
        return true;
    }

