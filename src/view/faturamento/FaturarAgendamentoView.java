    private void btnFaturarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFaturarActionPerformed
        // TODO add your handling code here:
        if (checkForm()) {
            faturamento.setDataFaturamento(MaskFormatTextUtil.dataTimeUs(new Date(System.currentTimeMillis())));
            faturamento.setFaturadoPor(SessaoUtil.getFuncionario());
            faturamento.setFormaPagamento((FormaPagamentoBean) jcbFormaPagamento.getSelectedItem());
            faturamento.setPorcentagemDesconto(Integer.parseInt(txtDesconto.getText()));
            faturamento.setValorTotal(MaskFormatTextUtil.changeCommaByDot(txtValorTotal.getText()));
            FaturamentoController.createFaturamento(faturamento);
            this.setVisible(false);
        } else {
            System.err.println("DADOS INVÃLIDOS");
        }
    }//GEN-LAST:event_btnFaturarActionPerformed

