    private void btnCadastrarHorarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarHorarioActionPerformed
        // TODO add your handling code here:
        if (!txtNovaHora.getText().equalsIgnoreCase("  :  ") || jlProfissionais.getSelectedIndex() != -1) {
            HorarioAgendaController.createNovoHorario(pro.getPessoaCodigo(), txtNovaHora.getText());
            redefHoras(pro.getPessoaCodigo());
        } else {
            JOptionPane.showMessageDialog(null, "Um profissional deve ser selecionado e um horário válido deve ser informado.");
        }
    }//GEN-LAST:event_btnCadastrarHorarioActionPerformed

    private void jlProfissionaisMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jlProfissionaisMouseClicked
        // TODO add your handling code here:
        Object[] value = jlProfissionais.getSelectedValues();
        pro = (ProfissionalBean) value[0];
        if (jlProfissionais.getSelectedIndex() != -1) {
            redefHoras(pro.getPessoaCodigo());
        }
    }//GEN-LAST:event_jlProfissionaisMouseClicked

