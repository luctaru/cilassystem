    private void btnExcluirHorarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirHorarioActionPerformed
        // TODO add your handling code here:
        if (jlProfissionais.getSelectedIndex() != -1 && jlHoras.getSelectedIndex() != -1) {
            HorarioAgendaController.deleteHorario(pro.getPessoaCodigo(), horario);
            redefHoras(pro.getPessoaCodigo());
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um horario para excluir.");
        }
    }//GEN-LAST:event_btnExcluirHorarioActionPerformed

