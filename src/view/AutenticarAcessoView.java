    /**
     * Checa o preenchimento dos campos do formulário.
     */
    private void autenticarDados() {
        if (!txtLogin.getText().equals("") && !txtSenha.toString().equals("")) {
            String login = txtLogin.getText();
            String senha = new String(txtSenha.getPassword());
            FuncionarioBean acesso = new FuncionarioBean(login, senha);
            if (FuncionarioController.autenticarAcesso(acesso)) {
                GerViewUtil.getViewMenuPrincipal();
                this.setVisible(false);
            } else {
                JOptionPane.showMessageDialog(null, "Dados incorretos. Cheque login e senha e tente novamente.");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Cheque login e senha e tente novamente.");
        }
    }

