    /**
     * Checa se existe funcionários do tipo administrador
     *
     * @return
     */
    private static boolean checkAdmFuncionario() {
        if (!FuncionarioController.existFuncionarioByCargo(1)) {
            JOptionPane.showMessageDialog(null, "Não existem funcionários de nível"
                    + " administrador cadastrado. Cadastre um administrador para prosseguir.");
            return false;
        }
        return true;
    }

