    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        if (checkForm()) {
            ProfissionalBean pro = new ProfissionalBean();
            pro.setNome(txtNome.getText());
            try {
                Date dataNas = new SimpleDateFormat("dd/MM/yyyy").parse(txtDataNascimento.getText());
                pro.setDataNascimento(dataNas);
            } catch (ParseException ex) {
                Logger.getLogger(CadastrarFuncionarioView.class.getName()).log(Level.SEVERE, null, ex);
            }
            if (jrbFem.isSelected()) {
                pro.setSexo("F");
            }
            if (jrbMas.isSelected()) {
                pro.setSexo("M");
            }
            pro.setCargo(new CargoBean(jcbCargo.getSelectedItem().toString()));
            pro.setLogin(txtLogin.getText());
            pro.setSenha(new String(txtSenha.getPassword()));
            if (jcbCargo.getSelectedItem().toString().equals("Profissional realizante")) {
                pro.setConselho(new ConselhoRegionalBean(jcbConselho.getSelectedItem().toString()));
                pro.setNumConselho(Integer.parseInt(txtNumConselho.getText()));
                pro.setEspecializacao(new EspecializacaoBean(jcbEspecializacao.getSelectedItem().toString()));
                pro.setUfConselho(jcbUf.getSelectedItem().toString());
            }
            FuncionarioController.createFuncionario(pro);
            this.setVisible(false);
            if(!GerViewUtil.checkIfGerFunOpen()){
                System.exit(1);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Por favor, preencha todos os dados");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

