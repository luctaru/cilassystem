    /**
     * Liberar ou bloquear parte do formulário para Profissional realizante conforme
     * mudança de seleção.
     */
    private void checkFormMedRealizante() {
        if (jcbCargo.getSelectedItem().toString().equals("Profissional realizante")) {
            unblockFormMedRealizante();
        } else {
            blockFormMedRealizante();
        }
    }

    /**
     * Bloquear parte do formulário para Profissional realizante
     */
    private void blockFormMedRealizante() {
        jcbConselho.setEnabled(false);
        txtNumConselho.setEnabled(false);
        jcbEspecializacao.setEnabled(false);
        jcbUf.setEnabled(false);
    }

    /**
     * Liberar parte do formulário para Profissional realizante
     */
    private void unblockFormMedRealizante() {
        jcbConselho.setEnabled(true);
        txtNumConselho.setEnabled(true);
        jcbEspecializacao.setEnabled(true);
        jcbUf.setEnabled(true);
    }

    /**
     * Método para válidar o preenchimento do formulário.
     *
     * @return
     */
    private boolean checkForm() {
        if (txtNome.getText().isEmpty()) {
            return false;
        }
        if (txtDataNascimento.getText().equalsIgnoreCase("  /  /    ")) {
            JOptionPane.showMessageDialog(null, "Data de nascimento inválida!");
            return false;
        }
        if (!jrbFem.isSelected() && !jrbMas.isSelected()) {
            return false;
        }
        if (txtLogin.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Informe o login.");
            return false;
        }
        if (txtSenha.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Informe uma senha.");
            return false;
        }
        if (jcbCargo.getSelectedItem().toString().equals("Profissional realizante.")) {
            if (jcbConselho.getSelectedIndex() == -1) {
                JOptionPane.showMessageDialog(null, "Selecione um conselho.");
                return false;
            }
            if (txtNumConselho.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Informe o número do conselho.");
                return false;
            }
            if (jcbEspecializacao.getSelectedIndex() == -1) {
                JOptionPane.showMessageDialog(null, "Selecione uma especialização.");
                return false;
            }
            if (jcbUf.getSelectedIndex() == -1) {
                JOptionPane.showMessageDialog(null, "Selecione a UF do conselho.");
                return false;
            }
        }
        return true;
    }

