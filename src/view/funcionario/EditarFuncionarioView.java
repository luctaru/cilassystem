    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(checkForm()){
            pro.setNome(txtNome.getText());
            try {
                Date dataNas = new SimpleDateFormat("dd/MM/yyyy").parse(txtDataNascimento.getText());
                pro.setDataNascimento(dataNas);
            } catch (ParseException ex) {
                Logger.getLogger(CadastrarFuncionarioView.class.getName()).log(Level.SEVERE, null, ex);
            }
            if (jrbFem.isSelected()) {
                pro.setSexo("F");
            }
            if (jrbMas.isSelected()) {
                pro.setSexo("M");
            }
            pro.setCargo(new CargoBean(jcbCargo.getSelectedItem().toString()));
            pro.setSenha(new String(txtSenha.getPassword()));
            FuncionarioController.updateFuncionario(this, pro);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    /**
     * Método para válidar o preenchimento do formulário.
     *
     * @return
     */
    private boolean checkForm() {
        if (txtNome.getText().isEmpty()) {
            return false;
        }
        if (txtDataNascimento.getText().equalsIgnoreCase("  /  /    ")) {
            JOptionPane.showMessageDialog(null, "Data de nascimento inválida!");
            return false;
        }
        if (!jrbFem.isSelected() && !jrbMas.isSelected()) {
            return false;
        }
        if (txtSenha.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Informe uma senha.");
            return false;
        }
        return true;
    }

