    /**
     * Preencher o formulário com dados do funcionário.
     *
     * @param fun
     */
    private void presetFuncionario() {
        txtNome.setText(pro.getNome());
        txtDataNascimento.setText(new SimpleDateFormat("dd/MM/yyyy").format(pro.getDataNascimento()));
        if (pro.getSexo().equalsIgnoreCase("F")) {
            jrbFem.setSelected(true);
        } else if (pro.getSexo().equalsIgnoreCase("M")) {
            jrbMas.setSelected(true);
        }
        GambUtil.setValueOfJComboBoxModel(jcbCargo, pro.getCargo().getDescricao());
        txtSenha.setText(pro.getSenha());
    }

    /**
     * Checar se fun é nulo. Se for a janela será fechada.
     */
    private void checkFun() {
        if (pro == null) {
            JOptionPane.showMessageDialog(null, "Nenhum funcionário selecionado anteriormente.");
            System.exit(1);
        }
    }

