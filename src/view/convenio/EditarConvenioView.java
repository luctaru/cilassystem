    private void btnExcluirConsultaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirConsultaActionPerformed
        // TODO add your handling code here:
        if (jtConPorCon.getSelectedRow() != -1) {
            int confirm = JOptionPane.showConfirmDialog(this, "Deseja realmente excluir esta consulta deste convênio?");
            switch (confirm) {
                case JOptionPane.YES_OPTION:
                    ConsultaPorConvenioController.deleteConsultaPorConvenio(conBycon);
                    break;
                default:
                    break;
            }
        } else {
            JOptionPane.showMessageDialog(null, "Nenhum exame foi selecionado.");
        }
    }//GEN-LAST:event_btnExcluirConsultaActionPerformed

