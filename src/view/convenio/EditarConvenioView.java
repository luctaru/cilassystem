    /**
     * Método usado para quando digitado um valor no campo de busca de
     * consultas, além de setar um model para tabela.
     */
    private void defConsultaPorCOnvenioTable() {
        tablemodelConPorCon = new ConsultaPorConvenioTableModel(conv.getNome());
        jtConPorCon.setModel(tablemodelConPorCon);
    }

    /**
     * Método que define o tamanho das colunas da tabela de convênios.
     */
    private void defColumnFuncionarioTable() {
        jtConPorCon.getColumnModel().getColumn(0).setPreferredWidth(400);
        jtConPorCon.getColumnModel().getColumn(1).setPreferredWidth(100);
    }

    /**
     * Preencher o formulário com dados do funcionário.
     *
     * @param conv
     */
    private void presetConvenio() {
        txtNome.setText(conv.getNome());
        txtCnpj.setText(conv.getCnpj());
        txtRazaoSocial.setText(conv.getRazaoSocial());
        txtTelefone.setText(conv.getTelefone());
        txtEmail.setText(conv.getEmail());
        txtCep.setText(conv.getCep());
        txtLogradouro.setText(conv.getLogradouro());
        txtNumResidencial.setText(conv.getNumeroResidencial());
        txtBairro.setText(conv.getBairro());
        txtCidade.setText(conv.getCidade());
        jcbUf.setSelectedItem(conv.getUf());
        conBycon = new ConsultaPorConvenioBean();
        conBycon.setConvenio(new ConvenioBean(conv.getConvenioCodigo()));
    }

    /**
     * Checar se conv é nulo. Se for a janela será fechada.
     */
    private void checkCon() {
        if (conv == null) {
            JOptionPane.showMessageDialog(null, "Nenhum convênio selecionado anteriormente.");
            System.exit(1);
        }
    }

