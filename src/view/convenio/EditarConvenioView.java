    /**
     * Preencher campos de endereco com dados gerados a partir do CEP.
     */
    private void presetCep() {
        try {
            cep = WebServicesUtil.searchCepUtil(txtCep.getText());
            txtLogradouro.setText(cep.getLogradouro());
            txtBairro.setText(cep.getBairro());
            txtCidade.setText(cep.getCidade());
            jcbUf.setSelectedItem(cep.getUf());
        } catch (Exception ex) {
            resetCep();
        }
    }

    /**
     * Reseta valores dos campos de endereço do formulário.
     */
    private void resetCep() {
        txtLogradouro.setText("");
        txtBairro.setText("");
        txtCidade.setText("");
        jcbUf.setSelectedIndex(0);
    }

    /**
     * Método para válidar o preenchimento do formulário.
     *
     * @return
     */
    private boolean checkForm() {
        if (txtNome.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "O campo de nome não pode estar vázio.");
            return false;
        }
        if (txtCnpj.getText().length() != 18 || txtCnpj.getText().equalsIgnoreCase("  .   .   /    .  ")) {
            JOptionPane.showMessageDialog(null, "CNJP deve conter apenas 14 digitos númericos.");
            return false;
        }
        if (txtRazaoSocial.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "O campo da razão social não pode estar vázia.");
            return false;
        }
        if (!txtEmail.getText().isEmpty()) {
            if (!MaskFormatTextUtil.checkEmail(txtEmail.getText())) {
                JOptionPane.showMessageDialog(null, "E-mail não válido.");
                return false;
            }
        }
        if (txtTelefone.getText().length() > 13 || txtTelefone.getText().equalsIgnoreCase("(  )    -    ")) {
            JOptionPane.showMessageDialog(null, "O campo de telefone deve conter 10 digitos númericos.");
            return false;
        }

        return true;
    }

